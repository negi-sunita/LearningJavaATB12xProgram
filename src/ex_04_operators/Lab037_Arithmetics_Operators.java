package ex_04_operators;

public class Lab037_Arithmetics_Operators {
    public static void main(String[] args) {
        int a = 30;
        int b = 3;
        float c = 3.0f;

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a/b);
        System.out.println(a*b);
        System.out.println(a/c);
    }
}
