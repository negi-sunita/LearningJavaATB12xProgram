package ex_07_Increment_Decrement_Op;

public class Lab070_ID_Op_PRE {
    public static void main(String[] args) {

        int a = 10;
        int b = ++a;
        System.out.println(a);
        System.out.println(b);

        // ++a ->  a = a+1 , increment by 1

        //  Exp and Result Table
        // Line No | a | Result b
        // 6 | 10 | na
        // 7 | 11 | 11

    }
}
