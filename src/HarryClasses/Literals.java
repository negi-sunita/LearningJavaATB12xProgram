package HarryClasses;

public class Literals {
    public static void main(String[] args) {
        byte age = 34;
        int marks = 65;
        char name = 'B';
        long abc = 3434434234L;
        short number = 322;
        float f1 = 34.2f;
        double d1 = 3343423.34;
        boolean isActive = true;
        System.out.println("Print value for byte "+age);
        System.out.println("Print value for int "+marks);
        System.out.println("Print value for char "+name);
        System.out.println("Print value for long "+abc);
        System.out.println("Print value for short "+number);
        System.out.println("Print value for float "+f1);
        System.out.println("Print value for double "+d1);
        System.out.println("Print value for boolean "+isActive);

    }
}
